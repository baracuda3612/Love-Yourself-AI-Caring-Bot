"""FSM state definitions and transition rules."""

from __future__ import annotations

# T5.3: PLAN_FLOW tunnel reduced to DATA_COLLECTION only.
# CONFIRMATION_PENDING and FINALIZATION removed — dead under LEGACY_PLAN_FLOW_ENABLED=False.
PLAN_FLOW_STATES = {
    "PLAN_FLOW:DATA_COLLECTION",
}

ADAPTATION_SELECTION = "ADAPTATION_SELECTION"
ADAPTATION_PARAMS = "ADAPTATION_PARAMS"
ADAPTATION_CONFIRMATION = "ADAPTATION_CONFIRMATION"
SCHEDULE_ADJUSTMENT = "SCHEDULE_ADJUSTMENT"

ADAPTATION_FLOW_STATES = {
    ADAPTATION_SELECTION,
    ADAPTATION_PARAMS,
    ADAPTATION_CONFIRMATION,
}

ADAPTATION_FLOW_ALLOWED_TRANSITIONS = {
    ("ACTIVE", ADAPTATION_SELECTION),
    ("ACTIVE_PAUSED", ADAPTATION_SELECTION),
    (ADAPTATION_SELECTION, ADAPTATION_PARAMS),
    (ADAPTATION_SELECTION, ADAPTATION_CONFIRMATION),
    (ADAPTATION_SELECTION, "ACTIVE"),
    (ADAPTATION_PARAMS, ADAPTATION_CONFIRMATION),
    (ADAPTATION_PARAMS, "ACTIVE"),
    (ADAPTATION_CONFIRMATION, "ACTIVE"),
    (ADAPTATION_CONFIRMATION, ADAPTATION_PARAMS),
}

SCHEDULE_ADJUSTMENT_ALLOWED_TRANSITIONS = {
    ("ACTIVE", SCHEDULE_ADJUSTMENT),
    ("ACTIVE_PAUSED", SCHEDULE_ADJUSTMENT),
    (SCHEDULE_ADJUSTMENT, "ACTIVE"),
    (SCHEDULE_ADJUSTMENT, "ACTIVE_PAUSED"),
}

# States that allow entering the plan creation flow (PLAN_FLOW:DATA_COLLECTION)
# or transitioning directly to ACTIVE via create_plan().
# T5.3: IDLE_ONBOARDED removed (never set in code).
#        ACTIVE / ACTIVE_PAUSED removed (re-plan from active not supported in v5).
#        IDLE_NEW added (first-time user enters onboarding from here).
PLAN_CREATION_ENTRY_STATES = {
    "IDLE_NEW",
    "IDLE_FINISHED",
    "IDLE_DROPPED",
    "IDLE_PLAN_ABORTED",
}

ADAPTATION_ENTRY_STATES = {
    "ACTIVE",
    "ACTIVE_PAUSED",
}

ENTRY_PROMPT_ALLOWED_STATES = PLAN_CREATION_ENTRY_STATES | ADAPTATION_ENTRY_STATES

ADAPTATION_FLOW_ENTRY_STATES = ADAPTATION_ENTRY_STATES

PLAN_FLOW_ENTRYPOINTS = PLAN_CREATION_ENTRY_STATES

# T5.3: IDLE_ONBOARDED removed — never set by any production code path.
IDLE_STATES = {
    "IDLE_NEW",
    "IDLE_PLAN_ABORTED",
    "IDLE_FINISHED",
    "IDLE_DROPPED",
}

# T5.3: ACTIVE_CONFIRMATION and ACTIVE_PAUSED_CONFIRMATION removed —
# empty entrypoints, never set, no live transitions.
ACTIVE_STATES = {
    "ACTIVE",
}

PAUSE_STATES = {
    "ACTIVE_PAUSED",
}

ALLOWED_BASE_STATES = IDLE_STATES | ACTIVE_STATES | PAUSE_STATES

PREFIXED_STATES = {"PLAN_FLOW"}

FSM_ALLOWED_STATES = (
    ALLOWED_BASE_STATES
    | PLAN_FLOW_STATES
    | ADAPTATION_FLOW_STATES
    | {SCHEDULE_ADJUSTMENT}
)
